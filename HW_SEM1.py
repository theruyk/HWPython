# Задача 1: Найдите сумму цифр трехзначного числа.
# 123 -> 6 (1 + 2 + 3)
# 100 -> 1 (1 + 0 + 0)

nubmer = int(input('Input three-digit number '))
if (nubmer < 999) and (nubmer >99):
  hundreds = nubmer//100
  tens = nubmer//10
  print((nubmer % 10) + hundreds + (tens % 10))
else :print('It is not three-digit number')

